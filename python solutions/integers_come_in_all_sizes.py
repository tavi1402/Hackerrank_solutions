'''In this code, it computes 'a' raised to the power of 'b' and 'c' raised to the power of 'd', 
and then adds these two values together to obtain the final result, which is then printed.'''

# Read four integers 'a', 'b', 'c', and 'd' from the user as input.
a = int(input())
b = int(input())
c = int(input())
d = int(input())

# Calculate the result by raising 'a' to the power of 'b' and 'c' to the power of 'd', and then adding the two results.
result = (a ** b) + (c ** d)

# Print the result.
print(result)
